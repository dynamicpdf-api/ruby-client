module DynamicPDFApi
  #
  # Specifies horizontal alignment. Center alignment is the default value for alignments.
  #
  class Align
    #
    # Align left.
    #
    LEFT = 'left'.freeze

    #
    # Align center. This is default.
    #
    CENTER = 'center'.freeze

    #
    # Align right.
    #
    RIGHT = 'right'.freeze
  end
end
